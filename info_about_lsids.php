<p class="fieldheader">What is an LSID?</p><p>
Life Science Identifiers (LSIDs) provide a means to identify and locate a piece of biological data and/or metadata on the web. For a more detailed description
please refer to the <a href="http://lsids.sourceforge.net">LSID Resolution Project Homepage</a>. Present in
the 2008 Annual Checklist, Catalogue of Life LSIDs combined with its resolution service provide a persistent and location independent means to access
taxon metadata in the form of <a href="http://www.w3.org/RDF/">RDF</a> (Resource Description Framework) documents drawing reference to concepts
from the <a href="http://rs.tdwg.org/ontology/voc/TaxonConcept">TDWG LSID Ontology</a>.

<p>&nbsp;</p>

<p class="fieldheader">What can you do with an LSID?</p><p>
Unlike a URL, LSIDs do not currently have native browser support and thus require some form of client plugin or proxy web service in order for you
to retrieve the metadata response from the resolver. There are several online resolvers including the <a href="http://lsid.tdwg.org/">TDWG proxy</a>. We
recommend using the <a href="http://www.mozilla.com/firefox/">Firefox</a> browser with the <a href="http://sourceforge.net/project/showfiles.php?group_id=198923">LSID Plugin</a>.
Once installed, the Firefox plugin will allow LSIDs (with the "lsidres:" prefix) to be copied into the browser's address bar.
